-- Returns a person's given name, often called the first name.
DROP FUNCTION IF EXISTS givenName;
DELIMITER //
CREATE FUNCTION givenName()
    RETURNS TEXT
    NOT DETERMINISTIC
    READS SQL DATA
BEGIN
    -- This query to pseudo randomly choose a row from a table
    -- came from the excellent article written by Jan Kneschke at
    -- http://jan.kneschke.de/projects/mysql/order-by-rand/
    RETURN
        (SELECT name
         FROM givenName AS t
         INNER JOIN
             (SELECT 1 + FLOOR(RAND() * (SELECT MAX(id) FROM givenName)) AS id)
                AS r
         WHERE t.id >= r.id
         ORDER BY t.id
         LIMIT 1);
END//
DELIMITER ;


-- Returns a male person's given name, often called the first name.
-- The list of names came from the U.S. Social Security Administration.
