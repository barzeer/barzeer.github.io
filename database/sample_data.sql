-- Returns a psuedo random password. This is
-- NOT a secure way to generate a password.
DROP FUNCTION IF EXISTS password;
DELIMITER //
CREATE FUNCTION password()
    RETURNS TEXT
    NOT DETERMINISTIC
    NO SQL
BEGIN
    DECLARE n INT;
    DECLARE pw TEXT DEFAULT '';
    SET n = randInt(8, 12);
    WHILE n != 0 DO
        SET pw = CONCAT(pw, CHAR(randInt(32, 127)));
        SET n = n - 1;
    END WHILE;
    RETURN pw;
END//
DELIMITER ;
