-- -----------------------------------------------------
-- Schema sample
-- -----------------------------------------------------
DROP SCHEMA IF EXISTS sample ;

CREATE SCHEMA IF NOT EXISTS sample DEFAULT CHARACTER SET utf8mb4 ;
USE sample ;

-- -----------------------------------------------------
-- Table givenName
-- -----------------------------------------------------
DROP TABLE IF EXISTS givenName ;

CREATE TABLE IF NOT EXISTS givenName (
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR(45) NOT NULL,
    gender CHAR(1) NOT NULL,
    PRIMARY KEY (id),
    UNIQUE INDEX name_UNIQUE (name ASC, gender ASC),
    INDEX idx_gender (gender ASC))
ENGINE = InnoDB ;

-- -----------------------------------------------------
-- Table familyName
-- -----------------------------------------------------
DROP TABLE IF EXISTS familyName ;

CREATE TABLE IF NOT EXISTS familyName (
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR(45) NOT NULL,
    PRIMARY KEY (id),
    UNIQUE INDEX name_UNIQUE (name ASC))
ENGINE = InnoDB ;

-- -----------------------------------------------------
-- Table street
-- -----------------------------------------------------
DROP TABLE IF EXISTS street ;

CREATE TABLE IF NOT EXISTS street (
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR(45) NOT NULL,
    PRIMARY KEY (id),
    UNIQUE INDEX name_UNIQUE (name ASC))
ENGINE = InnoDB ;

-- -----------------------------------------------------
-- Table state
-- -----------------------------------------------------
DROP TABLE IF EXISTS state ;

CREATE TABLE IF NOT EXISTS state (
    id INT NOT NULL AUTO_INCREMENT,
    abbrev CHAR(2) NOT NULL,
    name VARCHAR(45) NOT NULL,
    PRIMARY KEY (id),
    UNIQUE INDEX abbrev_UNIQUE (abbrev ASC))
ENGINE = InnoDB ;

-- -----------------------------------------------------
-- Table zipCode
-- -----------------------------------------------------
DROP TABLE IF EXISTS zipCode ;

CREATE TABLE IF NOT EXISTS zipCode (
    id INT NOT NULL AUTO_INCREMENT,
    zipCode CHAR(5) NOT NULL,
    stateAbbrev CHAR(2) NOT NULL,
    city VARCHAR(45) NOT NULL,
    areaCode CHAR(3) NOT NULL,
    INDEX fk_zipcode_state_idx (stateAbbrev ASC),
    PRIMARY KEY (id),
    UNIQUE INDEX zipCode_UNIQUE (zipCode ASC),
    INDEX idx_stateAbbrev (stateAbbrev ASC),
    CONSTRAINT fk_zipcode_state
        FOREIGN KEY (stateAbbrev)
        REFERENCES state (abbrev)
        ON DELETE RESTRICT
        ON UPDATE CASCADE)
ENGINE = InnoDB ;

-- -----------------------------------------------------
-- Table emailProvider
-- -----------------------------------------------------
