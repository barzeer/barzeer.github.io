-- Returns either TRUE or FALSE.
DROP FUNCTION IF EXISTS randBool;
DELIMITER //
CREATE FUNCTION randBool()
    RETURNS BOOLEAN
    NOT DETERMINISTIC
    NO SQL
BEGIN
    RETURN RAND() < 0.5;
END//
DELIMITER ;


-- Returns a pseudo random integer between low and high inclusive.
DROP FUNCTION IF EXISTS randInt;
DELIMITER //
CREATE FUNCTION randInt(low INT, high INT)
    RETURNS INT
    NOT DETERMINISTIC
    NO SQL
BEGIN
    RETURN low + FLOOR(RAND() * (high - low + 1));
END//
DELIMITER ;


-- Returns a value rounded to a fractional value.
-- For example roundToFrac(12.167, 0.25) returns 12.25
DROP FUNCTION IF EXISTS roundToFrac;
DELIMITER //
CREATE FUNCTION roundToFrac(value DOUBLE, frac DOUBLE)
    RETURNS DOUBLE
    NOT DETERMINISTIC
    NO SQL
BEGIN
    RETURN ROUND(value / frac) * frac;
END//
DELIMITER ;


-- Returns either 'M' or 'F'.
