CREATE FUNCTION phone(area TEXT)
    RETURNS TEXT
    NOT DETERMINISTIC
    NO SQL
BEGIN
    DECLARE prefix, four TEXT;
    SET prefix = CAST(randInt(100, 999) AS CHAR);
    SET four = CAST(randInt(1, 9999) AS CHAR);
    SET four = CONCAT(SUBSTR('000', 1, 4 - CHAR_LENGTH(four)), four);
    RETURN CONCAT(area, prefix, four);
END//
