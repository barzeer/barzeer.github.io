-- Returns a pseudo random email address.
DROP FUNCTION IF EXISTS email;
DELIMITER //
CREATE FUNCTION email(givenName TEXT, familyName TEXT)
    RETURNS TEXT
    NOT DETERMINISTIC
    READS SQL DATA
BEGIN
    DECLARE len INT;
    DECLARE email, sep, provider TEXT;

    IF RAND() < 0.5 THEN
        IF RAND() < 0.5 THEN
            SET givenName = SUBSTR(givenName, 1, 1);
        END IF;
    ELSE
        IF RAND() < 0.5 THEN
            SET familyName = SUBSTR(familyName, 1, 1);
        END IF;
    END IF;

    CASE randInt(1, 4)
        WHEN 1 THEN SET sep = '';
        WHEN 2 THEN SET sep = '.';
        WHEN 3 THEN SET sep = '-';
        WHEN 4 THEN SET sep = '_';
    END CASE;

    IF RAND() < 0.5 THEN
        SET email = CONCAT(givenName, sep, familyName);
    ELSE
        SET email = CONCAT(familyName, sep, givenName);
    END IF;
    SET email = LCASE(email);

    SET provider =
        (SELECT name
         FROM emailProvider AS t
         INNER JOIN
         (SELECT 1 + FLOOR(RAND() * (SELECT MAX(id) FROM emailProvider)) AS id)
             AS r
         WHERE t.id >= r.id
         ORDER BY t.id
         LIMIT 1);

    RETURN CONCAT(email, '@', provider);
END//
DELIMITER ;


-- Returns a pseudo random phone number with the given area code.
