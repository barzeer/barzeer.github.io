-- Returns the first part of a street address such as '4708 Ridge Way'.
DROP FUNCTION IF EXISTS street1;
DELIMITER //
CREATE FUNCTION street1()
    RETURNS TEXT
    NOT DETERMINISTIC
    READS SQL DATA
BEGIN
    DECLARE low, number INT;
    DECLARE street, type TEXT;

    SET low = POW(10, randInt(1, 3));
    SET number = randInt(low, low * 10 - 1);

    SET street =
        (SELECT name
         FROM street AS t
         INNER JOIN
             (SELECT 1 + FLOOR(RAND() * (SELECT MAX(id) FROM street)) AS id)
                 AS r
         WHERE t.id >= r.id
         ORDER BY t.id
         LIMIT 1);

    SET type =
        (SELECT name
         FROM streetType AS t
         INNER JOIN
             (SELECT 1 + FLOOR(RAND() * (SELECT MAX(id) FROM streetType)) AS id)
                 AS r
         WHERE t.id >= r.id
         ORDER BY t.id
         LIMIT 1);

    RETURN CONCAT(number, ' ', street, ' ', type);
END//
DELIMITER ;


