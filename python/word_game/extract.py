def extract_unknown_words():
    #filename = input("Name of file that contains text: ")
    filename = "book.txt"
    with open(filename, "rt", encoding="utf8") as text_file:
        text1 = text_file.read().lower()

    # Keep only space characters and English letters. Remove all other
    # characters.
    text2 = re.sub(r"[^\sa-z]+", " ", text1)

    # Keep only five letter words. Remove all other words.
    text3 = re.sub(r"\b([a-z]{1,4}|[a-z]{6,})\b", "", text2)

    # Replace all whitespace characters with a space.
    text4 = re.sub(r"\s+", " ", text3)

    words_set = set(text4.split())
    new_words_set = words_set - _KNOWN_WORDS_SET

    for word in sorted(new_words_set):
        print(word)


