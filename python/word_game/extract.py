def print_words_set(set_name, words_set):
    per_line = 5
    i = per_line
    sep = "    "
    print(set_name, "= {")
    for word in sorted(words_set):
        print(f'{sep}"{word}",', end="")
        i -= 1
        if i == 0:
            sep = "\n    "
            i = per_line
        else:
            sep = " "
    print()
    print("}")
    print()


